// FlippingMatrix

// Matrix A, N rows x M columns
// can change values by FLIPPING WHOLE COLUMNS (any number thereof, freely selected)

// Find how many rows can be made uniform (all 0s or 1s) by flipping columns

// Write an efficient algorithm for the following assumptions :
//
// N and M are integers within the range[1..100, 000];
// N* M is not greater than 100, 000.

struct node
{
    int max_uniform_rows = 0;
    map<int, node*> subnodes;
};

int solution(vector< vector<int> >& A)
{
    size_t N = A.size(), M = A.front().size();
    node head; // root of column-flipping-effects tree
    set<node*> outcomes; // leaf nodes of the tree, one of them will be the solution

    for (auto row : A)
    {
        vector<int> columns_flipped_to_make_row_uniform_ones;
        vector<int> columns_flipped_to_make_row_uniform_zeroes;

        // Make one allocation to avoid many grow stages if M is huge;
        // perf tradeoff, correctness is not affected if changed/removed
        columns_flipped_to_make_row_uniform_ones.reserve(M / 2);
        columns_flipped_to_make_row_uniform_zeroes.reserve(M / 2);

        for (size_t i = 0; i < M; ++i)
        {
            if (row[i] == 0)
            {
                columns_flipped_to_make_row_uniform_ones.push_back(i);
            }
            else
            {
                columns_flipped_to_make_row_uniform_zeroes.push_back(i);
            }
        }

        // Column with index M is virtual, nodes with this index are leaf nodes (outcomes)
        columns_flipped_to_make_row_uniform_ones.push_back(M); 
        columns_flipped_to_make_row_uniform_zeroes.push_back(M);

        // Grow the a node tree with root equal to head, documenting that flipping 
        // exactly these columns will make exactly one more row uniform
        auto current_node = &head;
        for (auto column_index : columns_flipped_to_make_row_uniform_ones)
        {
            if (current_node->subnodes.count(column_index) == 0)
            {
                current_node->subnodes[column_index] = new node;
            }

            current_node = current_node->subnodes[column_index];
            current_node->max_uniform_rows++;
        }

        outcomes.insert(current_node);

        // And once more for zeroes
        current_node = &head;
        for (auto column_index : columns_flipped_to_make_row_uniform_zeroes)
        {
            if (current_node->subnodes.count(column_index) == 0)
            {
                current_node->subnodes[column_index] = new node;
            }

            current_node = current_node->subnodes[column_index];
            current_node->max_uniform_rows++;
        }

        outcomes.insert(current_node);
    }

    // Now just find the best outcome, done
    auto best_outcome = &head;
    for (auto outcome : outcomes)
    {
        if (best_outcome->max_uniform_rows < outcome->max_uniform_rows)
        {
            best_outcome = outcome;
        }
    }

    return best_outcome->max_uniform_rows;
}

